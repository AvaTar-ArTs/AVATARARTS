#!/bin/bash
# Setup Shell Aliases for Workspace

echo "?? Adding workspace aliases to ~/.zshrc..."
echo ""

# Check if already added
if grep -q "# WORKSPACE ALIASES" ~/.zshrc 2>/dev/null; then
  echo "??  Aliases already exist in ~/.zshrc"
  echo "   Remove old ones first or skip this step"
  exit 0
fi

# Add aliases
cat >> ~/.zshrc << 'ALIASES'

# ????????????????????????????????????????????????????????????????
# WORKSPACE ALIASES - Added November 3, 2025
# ????????????????????????????????????????????????????????????????

# Navigation shortcuts
alias ws="cd ~/workspace/"
alias work="cd ~/workspace/work/"
alias ai="cd ~/workspace/advanced-systems/"
alias biz="cd ~/workspace/ECOSYSTEM/"

# Quick actions
alias launch="~/workspace/launch.sh"
alias dash="~/workspace/daily_dashboard.sh"
alias status="cat ~/workspace/ECOSYSTEM/ECOSYSTEM-STATUS.md"

# Revenue tracking
alias revenue="python ~/workspace/revenue_dashboard_v2.py"
alias addclient="python ~/workspace/revenue_dashboard_v2.py add"

# Testing & Setup
alias testall="python ~/workspace/test_everything.py"
alias apis="source ~/.env.d/loader.sh"

# Quick edits
alias editapis="nano ~/.env.d/llm-apis.env"
alias rebuildapis="bash ~/.env.d/rebuild_master.sh && source ~/.env.d/loader.sh"

# Project shortcuts
alias empire="cd ~/workspace/work/universal-sales-empire/"
alias clean="cd ~/workspace/work/cleanconnect-pro/"
alias album="cd ~/workspace/work/album-ai/"
alias heavenly="cd ~/workspace/work/heavenlyhands/"

# ????????????????????????????????????????????????????????????????
ALIASES

echo "? Aliases added to ~/.zshrc"
echo ""
echo "Reload your shell:"
echo "  source ~/.zshrc"
echo ""
echo "Then try:"
echo "  ws          ? cd ~/workspace/"
echo "  launch      ? Run interactive launcher"
echo "  dash        ? Daily dashboard"
echo "  revenue     ? Show revenue"
echo "  empire      ? Go to Universal Empire"
echo "  testall     ? Test everything"
echo ""
